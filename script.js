const supportedLanguages = ["zh", "en"];
const storedLanguage = localStorage.getItem("caifuclaw-language");
const browserLanguage = navigator.language || navigator.userLanguage || "";
const initialLanguage = supportedLanguages.includes(storedLanguage)
  ? storedLanguage
  : browserLanguage.toLowerCase().startsWith("zh")
    ? "zh"
    : "en";

function setLanguage(language) {
  const activeLanguage = supportedLanguages.includes(language) ? language : "en";
  const dictionary = translations[activeLanguage];

  document.documentElement.lang = activeLanguage === "zh" ? "zh-CN" : "en";
  document.title = dictionary.pageTitle;
  document.querySelector('meta[name="description"]').setAttribute("content", dictionary.pageDescription);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((element) => {
    const key = element.getAttribute("data-i18n-aria");
    if (dictionary[key]) {
      element.setAttribute("aria-label", dictionary[key]);
    }
  });

  const toggle = document.querySelector("[data-lang-toggle]");
  toggle.textContent = activeLanguage === "zh" ? "EN" : "\u4e2d\u6587";
  toggle.setAttribute("aria-label", activeLanguage === "zh" ? "Switch to English" : "\u5207\u6362\u5230\u4e2d\u6587");

  document.querySelectorAll("[data-zh-only]").forEach((element) => {
    element.hidden = activeLanguage !== "zh";
  });

  localStorage.setItem("caifuclaw-language", activeLanguage);
}

document.querySelector("[data-lang-toggle]").addEventListener("click", () => {
  const currentLanguage = document.documentElement.lang.toLowerCase().startsWith("zh") ? "zh" : "en";
  setLanguage(currentLanguage === "zh" ? "en" : "zh");
});

const sidebarLinks = [...document.querySelectorAll(".sidebar a[href^='#']")];

function setActiveSidebarLink(hash = window.location.hash || "#overview") {
  sidebarLinks.forEach((link) => link.classList.remove("is-active"));
  const activeLink = sidebarLinks.find((link) => link.getAttribute("href") === hash);
  if (activeLink) {
    activeLink.classList.add("is-active");
  }
}

sidebarLinks.forEach((link) => {
  link.addEventListener("click", () => {
    setActiveSidebarLink(link.getAttribute("href"));
  });
});

window.addEventListener("hashchange", () => setActiveSidebarLink());
setActiveSidebarLink();

setLanguage(initialLanguage);
